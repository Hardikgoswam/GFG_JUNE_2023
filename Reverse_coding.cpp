class Solution {
  public:
    int sumOfNaturals(int n) {
        
        // sum of N natural number's formula is : n(n+1)/2
        int  x = 1e9+7;
        long long y = ((long long)(n%x)*((n%x)+1))/2;
        return y%x;
    }
};

